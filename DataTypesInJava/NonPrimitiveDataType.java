// Non-Primitive Data Type are also known as Reference Data Type.
// These Data Types consists (Array, String, Default)
class NonPrimitiveDataType{
    public static void main(String ars[]){
        String a = "Gaurav";
        String s1 = new String("Gaurav");
        System.out.println(a);
        System.out.println(s1);
    }
}